// Absolute (negatif jadi positif)
var result = Math.abs(-2.5)
    console.log(result);

// Power / pangkat
var powResult = Math.pow(2,8)
    console.log(powResult);

// Square root (akar)
var sqrResult = Math.sqrt(24)
    console.log(sqrResult);

// Cube Root (akar 3 bilangan)
var cbrResult = Math.cbrt(8)
    console.log(cbrResult);

// Round (pembulatan dengan mengikuti aturan matematika)
var roundResult = Math.round (4.8)
    console.log(roundResult);

// Floor (pembulatan ke bawah)
var floorResult = Math.floor (4.9)
    console.log(floorResult);

// Ceiling (pembulatan ke atas)
var ceilResult = Math.ceil (4.1)
    console.log(ceilResult);

// Min atau Max (untuk mencari nilai minimal atau maksimal)
var minResult = Math.min (2,6,5,3,6)
var maxResult = Math.max (2,6,5,3,6)
    console.log(minResult);
    console.log(maxResult)

